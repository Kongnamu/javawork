package hello;

//파일 이름의 첫글자는 대문자로 함 
//표기법(낙타 - 카멜표기법 이라고도 함) : 단어가 합성될때도 첫글자는 대문자로 함
public class Helloworld {

	//main() 함수 : 메인 함수가 있어야 메모리(주기억장치 : RAM)에서 실행
	public static void main(String[] args) {
		// 한줄 주석 - 문자를 출력하는 프로그램 만들기
        System.out.println("Hello~ Java"); // ln : 다음 줄에 출력
        System.out.println("안녕~ 자바");
        
        //자기 소개
        System.out.println("이름: 김정수");
        System.out.println("주소: 서울시 은평구 갈현로 333-6");
	}

}
